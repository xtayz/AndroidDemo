public class App {

    private String id;
    private String name;
    private String version;

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getId() {

        return id;
    }

    public String getName() {
        return name;
    }

    public String getVersion() {
        return version;
    }
}
